unsigned int Cube::VAO = 0;
bool Cube::ready = false;

Cube::Cube()
{
	Cube::init();
}

Cube::Cube(glm::vec3 loc, glm::quat rot, Shader s, Tex2D t) : Shape(loc, rot, s, t) {
	Cube::init();
}

Cube::Cube(glm::vec3 loc, glm::quat rot, Shader s, glm::vec3 c) : Shape(loc, rot, s, c) {
	Cube::init();
}

void Cube::draw(glm::mat4 proj, glm::mat4 view)
{
	if(textured)
		texture.use();
	shader.use();
	shader.setMat4("projection", proj);
	shader.setMat4("view", view);
	shader.setMat4("model", model);
	shader.setBool("textured", textured);
	shader.setVec3("objectcolor", color);
	glBindVertexArray(VAO);
	glDrawArrays(GL_TRIANGLES, 0, 36);
}

